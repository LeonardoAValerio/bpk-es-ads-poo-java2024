public class Endereco {
    public String rua;
    public String numero;
    public String bairro;
    public String cidade;
    public String estado;

    public Endereco(String rua, String numero, String bairro, String cidade, String estado) {
        this.rua = rua;
        this.numero = numero;
        this.bairro = bairro;
        this.cidade = cidade;
        this.estado = estado;
    }
}
